//! Japanese sentence corpus with Korean (Hangul) phonetic annotations.
//!
//! This module provides training data for building the embedding vectors:
//! - Japanese sentences segmented into words
//! - Each word has: surface (kanji), reading (hiragana), hangul transcription
//! - Sentences span everyday topics to capture natural co-occurrence patterns

/// A single word in a sentence.
#[derive(Debug, Clone)]
pub struct CorpusWord {
    /// Display form (kanji or kana).
    pub surface: &'static str,
    /// Reading in hiragana.
    pub reading: &'static str,
    /// Korean (Hangul) phonetic transcription.
    pub hangul: &'static str,
}

/// A sentence in the corpus.
#[derive(Debug, Clone)]
pub struct CorpusSentence {
    /// Category/topic for the sentence.
    pub category: &'static str,
    /// The words composing this sentence.
    pub words: Vec<CorpusWord>,
}

impl CorpusSentence {
    /// Get all surface forms as a string slice vector.
    pub fn surfaces(&self) -> Vec<&str> {
        self.words.iter().map(|w| w.surface).collect()
    }
}

macro_rules! w {
    ($surface:expr, $reading:expr, $hangul:expr) => {
        CorpusWord {
            surface: $surface,
            reading: $reading,
            hangul: $hangul,
        }
    };
}

macro_rules! sentence {
    ($cat:expr, $( ($s:expr, $r:expr, $h:expr) ),+ $(,)?) => {
        CorpusSentence {
            category: $cat,
            words: vec![ $( w!($s, $r, $h) ),+ ],
        }
    };
}

