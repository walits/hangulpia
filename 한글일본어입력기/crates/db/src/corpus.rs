/// Get all unique words in the corpus as (surface, reading, hangul) tuples.
pub fn unique_words(corpus: &[CorpusSentence]) -> Vec<(&str, &str, &str)> {
    let mut seen = std::collections::HashSet::new();
    let mut words = Vec::new();
    for sentence in corpus {
        for w in &sentence.words {
            if seen.insert(w.surface) {
                words.push((w.surface, w.reading, w.hangul));
            }
        }
    }
    words
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn corpus_not_empty() {
        let corpus = build_corpus();
        assert!(corpus.len() > 50, "Corpus should have 50+ sentences");
    }

    #[test]
    fn unique_words_deduplication() {
        let corpus = build_corpus();
        let words = unique_words(&corpus);
        // "です" appears in many sentences but should appear only once
        let desu_count = words.iter().filter(|w| w.0 == "です").count();
        assert_eq!(desu_count, 1);
    }

    #[test]
    fn all_words_have_readings() {
        let corpus = build_corpus();
        for s in &corpus {
            for w in &s.words {
                assert!(!w.reading.is_empty(), "Word {} has empty reading", w.surface);
                assert!(!w.hangul.is_empty(), "Word {} has empty hangul", w.surface);
            }
        }
    }

    #[test]
    fn categories_covered() {
        let corpus = build_corpus();
        let categories: std::collections::HashSet<&str> = corpus.iter().map(|s| s.category).collect();
        assert!(categories.contains("greeting"));
        assert!(categories.contains("nature"));
        assert!(categories.contains("food"));
        assert!(categories.contains("travel"));
        assert!(categories.contains("daily"));
        assert!(categories.contains("body"));
        assert!(categories.contains("emotion"));
    }
}
